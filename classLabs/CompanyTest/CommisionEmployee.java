package s;

public class CommisionEmployee {

		private String name;
		private String ID;
		private double salary;
		
		public void setName(String n)
		{
			name = n;
		}
		
		public void setID(String id)
		{
			ID = id;
		}
		
		public void setSalary(double s)
		{
			salary = s;
		}
		
		public String getName()
		{
			return name;
		}
		
		public String getID()
		{
			return ID;
		}
		public double getSalary()
		{
			return salary;
		}

}
